package Variables;

public class YearstoSeconds {

    public static void main(String [] args) {

        double year;

        year = 365.0 * 24 * 60 * 60;


        System.out.println("0.1           year is  " + (0.1 * year) +            " seconds ");
        System.out.println("1             year is  " + (1 * year) +              " seconds ");
        System.out.println("5             year is  " + (5 * year) +              " seconds ");
        System.out.println("1000          year is  " + (1000 * year) +           " seconds ");
        System.out.println("1980          year is  " + (1980 * year) +           " seconds ");
        System.out.println("1991          year is  " + (1991 * year) +           " seconds ");
        System.out.println("2019          year is  " + (2019 * year) +           " seconds ");
        System.out.println("2021          year is  " + (2021 * year) +           " seconds ");
        System.out.println("11111         year is  " + (11111 * year) +          " seconds ");
        System.out.println("999999000000.0 year is " + (999999000000.0 * year) + " seconds ");


    }

    }
